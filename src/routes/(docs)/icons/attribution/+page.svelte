<h2>Penylo Icons Attribution</h2>
<p>
	Attribution to Penylo is required for all users. While not strictly required, we also highly
	recommend including attribution to the original rights owners where applicable.
</p>

<h3>Placement Guidelines</h3>
<p>
	We value clean design. Penylo allows you to place attribution anywhere it is visible to the end
	user, even if it is not immediately adjacent to the icons. For websites, placing a credit in the
	footer or on an about page is perfectly acceptable. For mobile applications, including the credit
	in your settings or legal menu is standard.
</p>

<h3>Recommended Text</h3>
<p>You may use the following templates for your attribution:</p>
<ul>
	<li><strong>Standard:</strong> "Logos provided by Penylo."</li>
	<li>
		<strong>Formal:</strong> "Logos are provided by Penylo and are the property of their respective rights
		holders; use does not imply endorsement."
	</li>
</ul>
<p>
	Feel free to vary the wording to fit your brand, just make sure that the attribution is clear.
</p>

<h3>Alternative Displays</h3>
<p>
	If you have a unique UI or an interesting way of displaying logos where a standard text disclaimer
	would be redundant or disruptive, Penylo allows for flexible integration. In these cases, you may
	satisfy the attribution requirement via a credits section in your documentation or a hover-state
	tooltip, provided the information remains accessible to the user, or any other method that is
	visible to the end user.
</p>

<h3>Exemptions</h3>
<p>
	Penylo does not currently provide exemptions to our attribution requirements to anyone but Penylo
	itself. If you feel you should have an exemption, please contact us.
</p>

